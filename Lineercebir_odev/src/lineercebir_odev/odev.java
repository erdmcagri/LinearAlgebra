    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int sonuc[][] = new int[birinciMatrisSatır][ikinciMatrisSütun];
        String satır1="";
         for ( c = 0 ; c < birinciMatrisSatır ; c++ )
         {
            for ( d = 0 ; d < ikinciMatrisSütun ; d++ )
            {   
               for ( k = 0 ; k < ikinciMatrisSatır ; k++ )
               {
                  toplam = toplam + matris1[c][k]*matris2[k][d];
               }

               sonuc[c][d] = toplam;
               toplam = 0;
               satır1=satır1+ " " +sonuc[c][d];
            }
            matris3Model.addElement(satır1);
            satır1="";
         }
         jList3.setModel(matris3Model);
    }//GEN-LAST:event_jButton1ActionPerformed

