    private void btn_matris1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_matris1ActionPerformed
        birinciMatrisSatır = Integer.parseInt(matrisBirSatır.getText());
        birinciMatrisSütun = Integer.parseInt(matrisBirSütun.getText());
        matris1=new int[birinciMatrisSatır][birinciMatrisSütun];
        String satır="";
        for (int i = 0; i < birinciMatrisSatır; i++) {
            for (int j = 0; j < birinciMatrisSütun; j++) {
                matris1[i][j]=Integer.parseInt(JOptionPane.showInputDialog("matrisin "+ (i+1)+ ". satır "+ (j+1)+". sütun elemanını giriniz :"));
                satır=satır+"  "+matris1[i][j];
            }
            matris1Model.addElement(satır);
            satır="";
        }
       jList1.setModel(matris1Model);

    }//GEN-LAST:event_btn_matris1ActionPerformed

